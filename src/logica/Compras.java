
package logica;



public class Compras {


private int numboletos; 
private double pboletos;

private String pelicula;
private String sala;
private String hora;
private int boleto_ID;

    public int getNumboletos() {
        return numboletos;
    }

    public void setNumboletos(int numboletos) {
        this.numboletos = numboletos;
    }

    public double getPboletos() {
        return pboletos;
    }

    public void setPboletos(double pboletos) {
        this.pboletos = pboletos;
    }

    public String getPelicula() {
        return pelicula;
    }

    public void setPelicula(String pelicula) {
        this.pelicula = pelicula;
    }

    public String getSala() {
        return sala;
    }

    public void setSala(String sala) {
        this.sala = sala;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public int getBoleto_ID() {
        return boleto_ID;
    }

    public void setBoleto_ID(int boleto_ID) {
        this.boleto_ID = boleto_ID;
    }


    
}


